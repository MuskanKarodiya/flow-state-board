import { serve } from "https://deno.land/std@0.168.0/http/server.ts";
import { createClient } from "https://esm.sh/@supabase/supabase-js@2";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers":
    "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages, tasks } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const authHeader = req.headers.get("Authorization");

    const tasksContext = tasks
      ? `\n\nCurrent tasks on the board:\n${JSON.stringify(tasks, null, 2)}`
      : "";

    const systemPrompt = `You are a helpful AI assistant integrated into a Kanban board app. You help users manage their tasks.
You can help users by:
1. Creating new tasks - respond with a JSON action block like: {"action":"create","title":"...","description":"...","priority":"low|medium|high","status":"todo|in_progress|complete"}
2. Editing tasks - respond with: {"action":"edit","taskId":"...","updates":{"title":"...","description":"...","priority":"...","progress":50}}
3. Moving tasks - respond with: {"action":"move","taskId":"...","status":"todo|in_progress|complete"}
4. Deleting tasks - respond with: {"action":"delete","taskId":"..."}
5. Suggesting tasks based on context

When performing actions, wrap the JSON in a code block with the language tag "action" like:
\`\`\`action
{"action":"create","title":"...","description":"...","priority":"medium","status":"todo"}
\`\`\`

Then also provide a friendly natural language response explaining what you did.

Keep responses concise and helpful. Be friendly and encouraging!${tasksContext}`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limits exceeded, please try again later." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI usage limit reached. Please add credits." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI gateway error" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
